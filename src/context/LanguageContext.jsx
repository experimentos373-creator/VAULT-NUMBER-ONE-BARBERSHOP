// Merge NEW_TRANSLATIONS into TRANSLATIONS recursively
Object.keys(NEW_TRANSLATIONS).forEach(lang => {
  if (TRANSLATIONS[lang]) {
    Object.assign(TRANSLATIONS[lang], NEW_TRANSLATIONS[lang]);
  }
});

export function LanguageProvider({ children }) {
  const location = useLocation();
  
  // Detect active language from URL prefix
  let language = "pt";
  const path = location.pathname;
  if (path.startsWith("/en") || path === "/en") {
    language = "en";
  } else if (path.startsWith("/es") || path === "/es") {
    language = "es";
  } else if (path.startsWith("/fr") || path === "/fr") {
    language = "fr";
  } else if (path.startsWith("/de") || path === "/de") {
    language = "de";
  }

  const t = (key) => {
    return TRANSLATIONS[language]?.[key] || TRANSLATIONS["pt"]?.[key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}
